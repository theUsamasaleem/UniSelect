	<script>
		$( document ).ready(function() {
			loadReviews();
		});
		
		function loadReviews(){
			
			var university = "maju";
			$.ajax({
				type: "post",
				url: "./functions/get_review.php",
				data: {university: university},
				success: function (data) {
					$( '#display_reviews' ).html(data);
				}

			});
			
			$("#commenter-message").val('');
		}
		
		function showSuccessMessage(){
			
			swal(
				'Congratulation',
				'Your review has been posted.',
				'success'
			)
			
		}
		
		function showLoginMessage(){
			swal(
					'Please Login!',
					'in order to post a review.',
					'warning'
				)			
		}
		
		function showErrorMessage(){
			swal(
					'Error Message!',
					'Please enter Your comment',
					'warning'
				)
		}
		
		function showErrorReview(){
			swal(
					'Error Message!',
					'Please select Rating',
					'warning'
				)
		}
		
		
		function postReviews(){
			
			if (<?php echo isset($_SESSION['userid'])?'true':'false'; ?>) {
				if(document.getElementById('commenter-message').value != ""){
					if($("input[type='radio'][name='rate']:checked").val()){
						showSuccessMessage();
						
						
						var comment = $("#commenter-message").val();
						var university = "maju";
						var rating = $("input[type='radio'][name='rate']:checked").val();
	
						$.ajax({
							type: "post",
							url: "./functions/post_review.php",
							data: {comment: comment, university: university, rating: rating},
							success: function (data) {
								showSuccessMessage();
								loadReviews();
							}

						});
						
					
					}
					else{
						showErrorReview();
					}
				}
				else{
					showErrorMessage();
				}
			  
			} else {
			  showLoginMessage();
			}		
		}
		
	</script>
